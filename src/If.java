public class If {
    public static void main(String[] args) {

        int num1 = 3435;
        int num2 = 50;


        if (num1 % 2 == 0) {
            System.out.println("num1 is not an odd number");
        }
        if (num2 % 2 == 0) {
            System.out.println("num2 is not an odd number");
        }
        if (num1 % 2 == 1) {
            System.out.println("num1 is an odd number");
        }
        if (num2 % 2 == 1) {
            System.out.println("num1 is an odd number");
        }
    }
}
